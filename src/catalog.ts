export const getStickersForGroup = (group: TeamGroup): string[] => {
  if (group.code === '00') {
    return ['00'];
  }
  const codes: string[] = [];
  const baseCode = group.code === 'FWC-A' || group.code === 'FWC-B' ? 'FWC' : group.code;
  for (let i = group.startNum; i <= group.endNum; i++) {
    codes.push(`${baseCode}${i}`);
  }
  return codes;
};

